
/**
 * An enum that represents the chart type. 
 */
export enum ChartType
{
    /**
     * The polar chart type. 
     */
    polar = 'Polar',

    /**
     * The vertical bar chart type. 
     */
    verticalBar = 'Vertical Bar',

    /**
     * The pie grid chart type. 
     */
    pieGrid = 'Pie Grid',

    /**
     * The gauge chart type. 
     */
    gauge = 'Gauge'
}
